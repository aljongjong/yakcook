package com.yakcook.product.vo;

public class ShoppingBasketProVo {
	
	private int shoppingBasketNo;
	private int productNo;
	private int inventory;

	
	
	public ShoppingBasketProVo() {
		this.shoppingBasketNo = shoppingBasketNo;
		this.productNo = productNo;
		this.inventory = inventory;
		
	}
	public int getShoppingBasketNo() {
		return shoppingBasketNo;
	}
	public void setShoppingBasketNo(int shoppingBasketNo) {
		this.shoppingBasketNo = shoppingBasketNo;
	}
	public int getProductNo() {
		return productNo;
	}
	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}
	public int getInventory() {
		return inventory;
	}
	public void setInventory(int inventory) {
		this.inventory = inventory;
	}


	
	
}
